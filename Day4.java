package javaday4;

public class Javaday4 {
    public static void main(String[] args) {
        // \n digunakan untuk membuat baris baru
        System.out.println("Hai kenalin aku Nabila.\nSenang berkenalan denganmu.");
        
        // \t untuk tab atau spasi
        System.out.println("Nama teman:\tZulfiah\tTitin\tDida\tDina\tElsa");
        
        /* \\Backlash untuk menampilkan karakter
        */
        System.out.println("File disimpan di: C:\\user\\nama user\\docx");
        
        // \"untuk menampilkan tanda kutip gaanda
        System.out.println("kakak berkata,\"Ayo belajar\"");
        
        
     }
    
}
